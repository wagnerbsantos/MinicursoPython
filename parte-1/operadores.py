print('Insira dois numeros')
numero1 = int(input())
numero2 = int(input())
soma = numero1 + numero2
multiplicacao = numero1 * numero2
divisao = numero1 / numero2
exponencial = numero1 ** numero2
divisao_inteira = numero1 // numero2
modulo = numero1 % numero2

print('O resultado da soma e: ' + str(soma))
print('O resultado da multiplicacao e: ' + str(multiplicacao))
print('O resultado da divisao e: ' + str(divisao))
print('O resultado da potencia e: ' + str(exponencial))
print('O resultado da divisao inteira e: ' + str(divisao_inteira))
print('O resto da divisao e: ' + str(modulo))
